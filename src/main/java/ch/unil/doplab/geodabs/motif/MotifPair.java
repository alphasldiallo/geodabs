package ch.unil.doplab.geodabs.motif;

public class MotifPair {

    public final int i;
    public final int j;
    public final int ie;
    public final int je;
    public final double d;

    public MotifPair(int i, int j, int ie, int je, double d) {
        this.i = i;
        this.j = j;
        this.ie = ie;
        this.je = je;
        this.d = d;
    }

}
